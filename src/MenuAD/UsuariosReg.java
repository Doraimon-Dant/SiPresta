            //registrar
            respemp = this.ControlEmp.Insertar(TxtNom.getText(), txtApellido.getText(),pasword, Estatus, TxtFech.getText(), TxtCalle.getText(), TxtNumExt.getText(), TxtColonia.getText(), String.valueOf(CmbCiudad.getSelectedItem()), TxtCodP.getText(), String.valueOf(CmbMunicipio.getSelectedItem()), String.valueOf(CmbEstado.getSelectedItem()), CE, TxtCel.getText(), id);
