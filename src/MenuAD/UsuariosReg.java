    public boolean verificar_email(String text){
        
        Pattern emailspatron= Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"+"[A-Za-z0-9-]+(\\.[A-Za-z0-9-]+)*(\\.[A-Za-z]{2,})$");
        Matcher MatE=emailspatron.matcher(text);
        return MatE.find();
    }
    public boolean verificar_Passw(String text){
        
        Pattern Passwpatron= Pattern.compile("^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&+=])(?=\\S+$).{1,8}$");
        Matcher MatP=Passwpatron.matcher(text);
        return MatP.find();
    }

