    private void TxtCEKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtCEKeyReleased
        if (TxtCE.getText().length() > 0) {
            if (verificar_email(TxtCE.getText())) {
                LblVerific.setText("El correo electronico es valido");
                LblVerific.setForeground(Color.GREEN);
                this.verificc = true;

            } else {
                LblVerific.setText("El correo electronico no es valido");
                LblVerific.setForeground(Color.RED);
                this.verificc = false;
            }
        } else {
            LblVerific.setText("");
            this.verificc = false;

        }
    }//GEN-LAST:event_TxtCEKeyReleased

    private void PSWKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PSWKeyReleased
        String Mensaje;

        if (PSW.getText().length() > 0) {
            if (verificar_Passw(PSW.getText())) {
                TxtVerific.setVisible(true);
                TxtVerific.setText("La contrasena es correcta");
                TxtVerific.setForeground(Color.GREEN);
                if (PSW.getText().equals(VerificacionPSW.getText())) {
                    TxtVerific.setText("");
                    TxtVerific.setText("Las Contrasenas Coinciden");
                    TxtVerific.setForeground(Color.GREEN);
                    this.verificp = true;
                } else {
                    TxtVerific.setText("");
                    TxtVerific.setText("Las contrasenas no coinciden");
                    TxtVerific.setForeground(Color.RED);
                }
            } else {
                TxtVerific.setVisible(true);
                TxtVerific.setText("");
                TxtVerific.setForeground(Color.RED);
                Mensaje = "La contraseña debe contener al menos: \n 1 Número del 0 al 9 \n Una Letra Mayúscula Una Minúscula \n Un caracter especial como: !#$%& \n Ser de al menos 8 dígitos";
                TxtVerific.setText(Mensaje);
                
            }
        } else {
            TxtVerific.setVisible(false);
            TxtVerific.setText("");
            this.verificp = false;
        }
    }//GEN-LAST:event_PSWKeyReleased

