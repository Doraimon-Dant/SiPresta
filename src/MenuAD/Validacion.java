    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(TxtBuscar.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Debes ingresar un nombre para buscar", "Sistema", JOptionPane.WARNING_MESSAGE);
            TxtBuscar.requestFocus();
            return;
        }
        this.listar(Integer.parseInt(TxtBuscar.getText()));
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAutoActionPerformed
        if (ContratoTabla.getSelectedRowCount() == 1) {
            int id = Integer.parseInt(String.valueOf(ContratoTabla.getValueAt(ContratoTabla.getSelectedRow(), 0)));
            this.CONTROLCONTR.Validar(id);
        }else{
            ContratoTabla.requestFocus();
            JOptionPane.showMessageDialog(this,"Favor de seleccionar un registro","Tabla Contrato",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAutoActionPerformed

