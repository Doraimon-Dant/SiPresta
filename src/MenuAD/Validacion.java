    private void btnArchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArchActionPerformed
       if (ContratoTabla.getSelectedRowCount() == 1) {
            String ruta = String.valueOf(ContratoTabla.getValueAt(ContratoTabla.getSelectedRow(), 4));
            try{
                Runtime.getRuntime().exec("cmd /c start "+ ruta);
            }catch(IOException e){
                e.printStackTrace();
            }
        }else{
            ContratoTabla.requestFocus();
            JOptionPane.showMessageDialog(this,"Favor de seleccionar un registro","Tabla Contrato",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnArchActionPerformed


